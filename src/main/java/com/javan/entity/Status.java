package com.javan.entity;

public class Status {
    int status;
    String msg;

    public void setstatus(int status) {
        this.status = status;
    }

    public int getstatus() {
        return status;
    }

    public String getMsg() {
        return msg;
    }

    public void setMsg(String msg) {
        this.msg = msg;
    }
}
